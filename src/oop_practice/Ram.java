package oop_practice;

public class Ram {
    String model;
    int size;

    public Ram(String model, int size) {
        this.model = model;
        this.size = size;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getModel() {
        return model;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getSize() {
        return size;
    }

}
